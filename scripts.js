var wrapper = document.getElementById("wrapper");
var container,
  counter = 0;

projects.forEach(function (data, ind) {
  if (counter % 4 === 0) {
    container = document.createElement("div");
    container.className = "container";
    wrapper.appendChild(container);
  }

  container.innerHTML += `
  <div class="card" id="${ind + 1}" style="background-image: url(${
    data.img
  });" alt="" onclick="tabdeel(this)"> 
    <div class="top">
      <h4 class="topp">${data.date}</h4>
    </div>  
    <div class="row">
      <div class="icon"><span>${ind + 1}</span></div>
      <div class="description">
        <h4>${data.title || ""}</h4>
        <p>SMIT</p>
      </div>
      <div class="buttons">
        <button onclick="event.stopPropagation(); window.open('${
          data.githubLink
        }', '_blank')">Github Code</button>
        <button onclick="event.stopPropagation(); window.open('${
          data.hostedLink
        }', '_blank')">Web View</button>
      </div>
    </div>
  </div>`;

  counter++;
});

var card = document.getElementsByClassName("card");

function tabdeel(element) {
  for (var i = 0; i < card.length; i++) {
    card[i].classList.remove("active");
    card[i].style.width = "220px";
    card[i].style.height = "400px";
  }
  element.classList.add("active");
  element.style.width = "600px";
  element.style.height = "400px"; // Adjust height if necessary
}
